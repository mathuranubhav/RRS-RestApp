package rrs.model;

public class Settings {

	private String orgName;
	private String orgPhone;
	private String orgEmail;
	private String orgAddress;
	private String	orgOpenDays;
	private String orgTimings;
	private int orgTableNo;
	private String orgAutoAssign;
	private String orgUsername;
	private String orgPassword;
	
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getOrgPhone() {
		return orgPhone;
	}
	public void setOrgPhone(String orgPhone) {
		this.orgPhone = orgPhone;
	}
	public String getOrgEmail() {
		return orgEmail;
	}
	public void setOrgEmail(String orgEmail) {
		this.orgEmail = orgEmail;
	}
	public String getOrgAddress() {
		return orgAddress;
	}
	public void setOrgAddress(String orgAddress) {
		this.orgAddress = orgAddress;
	}
	public String getOrgOpenDays() {
		return orgOpenDays;
	}
	public void setOrgOpenDays(String orgOpenDays) {
		this.orgOpenDays = orgOpenDays;
	}
	public String getOrgTimings() {
		return orgTimings;
	}
	public void setOrgTimings(String orgTimings) {
		this.orgTimings = orgTimings;
	}
	public int getOrgTableNo() {
		return orgTableNo;
	}
	public void setOrgTableNo(int orgTableNo) {
		this.orgTableNo = orgTableNo;
	}
	public String getOrgAutoAssign() {
		return orgAutoAssign;
	}
	public void setOrgAutoAssign(String orgAutoAssign) {
		this.orgAutoAssign = orgAutoAssign;
	}
	public String getOrgUsername() {
		return orgUsername;
	}
	public void setOrgUsername(String orgUsername) {
		this.orgUsername = orgUsername;
	}
	public String getOrgPassword() {
		return orgPassword;
	}
	public void setOrgPassword(String orgPassword) {
		this.orgPassword = orgPassword;
	}
	
}
